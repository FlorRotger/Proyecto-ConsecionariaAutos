    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        //traemos todos los datos del auto 
        String modelo = txtModelo.getText();
        String marca = txtMarca.getText();
        String motor = txtMotor.getText();
        String color = txtColor.getText();
        String patente = txtPatente.getText();
        
        int cantPuertas = Integer.parseInt(txtCantPuertas.getText());

        //llamo a la logica
        control.modificarAuto(auto, modelo, marca, motor, color, patente, cantPuertas);

        //cartel q se guardo correctamente
        mostrarMensaje("Edición realizada correctamente", "Info", "Ediciòn exitosa");
        
        //muesto pantalla consulta
        
        ConsultaAutomovil consul = new ConsultaAutomovil();
        consul.setVisible(true);
        consul.setLocationRelativeTo(null);
        
        //cerramos ventana modif
        this.dispose();
    }//GEN-LAST:event_btnModificarActionPerformed

